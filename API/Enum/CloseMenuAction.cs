namespace CS2MenuManager.API.Enum;

/// <summary>
/// Defines the actions that can be taken when a menu is closed.
/// </summary>
public enum CloseMenuAction
{
    /// <summary>
    /// Closes the menu.
    /// </summary>
    Close,

    /// <summary>
    /// Resets the menu to its original state.
    /// </summary>
    Reset,
}